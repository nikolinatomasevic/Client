	private void pripremiFormu() throws Exception {
		cmbRadnoMesto.removeAllItems();
		List<RadnoMesto> radnaMesta = Controller.getInstance().ucitajListuRadnihMesta();
		radnaMesta.remove(0);
		for (RadnoMesto radnoMesto : radnaMesta) {
			cmbRadnoMesto.addItem(radnoMesto);
		}
	}

	public void setRadnik(Radnik radnik) {
		lblID.setText(String.valueOf(radnik.getRadnikID()));
		txtIme.setText(radnik.getIme());
		txtPrezime.setText(radnik.getPrezime());
		txtBrojTelefona.setText(radnik.getBrojTelefona());
		txtMejl.setText(radnik.getMejl());
		if (radnik.getRadnoMesto().equals(new RadnoMesto(1l, "Radnik u magacinu"))) {
			lblRadnoMestoCmb.setVisible(false);
			cmbRadnoMesto.setVisible(false);
			lblRadnoMesto.setText(String.valueOf(radnik.getRadnoMesto()));
		} else {
			lblRadnoMestoLbl.setVisible(false);
			lblRadnoMesto.setVisible(false);
			cmbRadnoMesto.setSelectedItem(radnik.getRadnoMesto());
		}
	}

