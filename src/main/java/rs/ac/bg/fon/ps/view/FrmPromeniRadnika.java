	private void btnSacuvajPromeneActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSacuvajPromeneActionPerformed
		try {
			Radnik radnik = new Radnik();
			radnik.setRadnikID(Long.valueOf(lblID.getText()));
			radnik.setIme(txtIme.getText().trim());
			radnik.setPrezime(txtPrezime.getText().trim());
			if (!txtBrojTelefona.getText().trim().isEmpty()) {
				radnik.setBrojTelefona(txtBrojTelefona.getText().trim());
			}
			radnik.setMejl(txtMejl.getText().trim());
			if (cmbRadnoMesto.isVisible()) {
				radnik.setRadnoMesto((RadnoMesto) cmbRadnoMesto.getSelectedItem());
			} else {
				radnik.setRadnoMesto(new RadnoMesto(1l, "Radnik u magacinu"));
			}
			Controller.getInstance().promeniRadnika(radnik);
			JOptionPane.showMessageDialog(this, "Sistem je zapamtio radnika.", "Info", JOptionPane.INFORMATION_MESSAGE);
			this.dispose();
		} catch (Exception ex) {
			JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_btnSacuvajPromeneActionPerformed

